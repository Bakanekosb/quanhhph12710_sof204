    void loadComboboxChuyenDe() {
        DefaultComboBoxModel cbo_modelChuyenDe = (DefaultComboBoxModel) cbo_chuyenDe.getModel();
        cbo_modelChuyenDe.removeAllElements();
        try {
            lst_chuyenDe = chuyenDeDAO.selectAll();
            for (ChuyenDe x : lst_chuyenDe) {
                cbo_modelChuyenDe.addElement(x.getTenCD());
            }
            loadComboboxKhoaHoc();
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    void changeCombobox() {
        cbo_chuyenDe.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                loadComboboxKhoaHoc();                
            }
        });
    }

    void loadComboboxKhoaHoc() {
        DefaultComboBoxModel cbo_modelKhoaHoc = (DefaultComboBoxModel) cbo_khoaHoc.getModel();
        cbo_modelKhoaHoc.removeAllElements();
        String maCD = lst_chuyenDe.get(cbo_chuyenDe.getSelectedIndex()).getMaCD();
        try {
            lst_khoaHoc = khoaHocDAO.selectByMaCD(maCD);
            for (KhoaHoc x : lst_khoaHoc) {
                cbo_modelKhoaHoc.addElement(new Utils().getTenKH(x.getMaKH()));
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
