    void loadTable_nguoiHoc(){
        tblModel_nguoiHoc.setRowCount(0);
        try {
            lst_nguoiHoc = nguoiHocDAO.selectByKeyword(keyword);
            for (NguoiHoc x : lst_nguoiHoc) {
                tblModel_nguoiHoc.addRow(new Object[]{x.getMaNH(), x.getHoTen(), x.isGioiTinh() ? "Nam" : "Nữ",
                    DateHelper.toString(x.getNgaySinh()), x.getDienThoai(), x.getEmail()});
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    private void init() {
        this.setLocationRelativeTo(null);
        this.setIconImage(XImage.getAppIcon());
        setTitle("QUẢN LÝ KHÓA HỌC");
        tbl_danhSachHocVien.setModel(tblModel_hocVien);
        tbl_danhSachNguoiHoc.setModel(tblModel_nguoiHoc);
        loadComboboxChuyenDe();
        loadTable_nguoiHoc();
        changeCombobox();
    }

