    void loadComboboxKhoaHoc() {
        DefaultComboBoxModel cbo_modelKhoaHoc = (DefaultComboBoxModel) cbo_khoaHoc.getModel();
        cbo_modelKhoaHoc.removeAllElements();
        String maCD = lst_chuyenDe.get(cbo_chuyenDe.getSelectedIndex()).getMaCD();
        try {
            lst_khoaHoc = khoaHocDAO.selectByMaCD(maCD);
            if (lst_khoaHoc.isEmpty() || lst_khoaHoc == null) {
                cbo_modelKhoaHoc.addElement("Chưa có khóa học nào");
                return;
            }
            cbo_modelKhoaHoc.addElement("Mời chọn khóa học:");
            for (KhoaHoc x : lst_khoaHoc) {
                cbo_modelKhoaHoc.addElement(new Utils().getTenKH(x.getMaKH()));
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn HocVien!");
        }
//        loadTable_hocVien();
    }

    void loadTable_hocVien() {
        tblModel_hocVien.setRowCount(0);
        try {
            maKH = lst_khoaHoc.get(cbo_khoaHoc.getSelectedIndex() - 1).getMaKH();
            System.out.println(maKH);
            lst_hocVien = hocVienDAO.selectByCourse(maKH);
            for (HocVien x : lst_hocVien) {
                tblModel_hocVien.addRow(new Object[]{lst_hocVien.indexOf(x) + 1, x.getMaHV(), x.getMaNH(),
                    nguoiHocDAO.selectById(x.getMaNH()).getHoTen(), x.getDiem()});
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu loadTable_hocVien!");
        }
    }

     void loadTable_nguoiHoc() {
        tblModel_nguoiHoc.setRowCount(0);
        try {
            totalPage = (int) Math.ceil((double) nguoiHocDAO.getTotalRow(keyword) / rowsOfPage);
            if(pageNumber > totalPage){
                pageNumber = 1;
            }
            lst_nguoiHoc = nguoiHocDAO.selectByKeyword(keyword, pageNumber, 10);
            for (NguoiHoc x : lst_nguoiHoc) {
                tblModel_nguoiHoc.addRow(new Object[]{x.getMaNH(), x.getHoTen(), x.isGioiTinh() ? "Nam" : "Nữ",
                    DateHelper.toString(x.getNgaySinh()), x.getDienThoai(), x.getEmail()});
            }
            btnStatus();
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu loadTable_nguoiHoc!");
        }
    }
    
    void changePage(String button) {
        switch (button) {
            case "first": {
                pageNumber = 1;
                break;
            }
            case "prev": {
                pageNumber--;
                break;
            }
            case "next": {
                pageNumber++;
                break;
            }
            case "last": {
                pageNumber = totalPage;
                break;
            }
        }
        loadTable_nguoiHoc();
        
    }
    
    void btnStatus() {
        txt_soTrang.setText(pageNumber + "");
        lbl_tongSoTrang.setText("/" + totalPage + "");
        btn_firstPage.setEnabled(pageNumber > 1);
        btn_prevPage.setEnabled(pageNumber > 1);
        btn_lastPage.setEnabled(pageNumber < totalPage);
        btn_nextPage.setEnabled(pageNumber < totalPage);
    }

