    @Override
    public void insert() {
        StringBuilder sb = new StringBuilder();
        if (cbo_khoaHoc.getSelectedIndex() == 0 || !cbo_khoaHoc.isValid()) {
            DialogHelper.alert(this, "Cần chọn khóa học trước!");
            return;
        }
        int[] rowIndex_nguoiHoc = tbl_danhSachNguoiHoc.getSelectedRows();
        for (int i : rowIndex_nguoiHoc) {
            boolean hocVienTonTai = false;
            String maNH = lst_nguoiHoc.get(i).getMaNH();
            for (HocVien x : lst_hocVien) {
                if (maNH.equals(x.getMaNH())) {
                    sb.append(maNH + "đã có trong khóa học!\n");
                    hocVienTonTai = true;
                }
            }
            if (!hocVienTonTai) {
                HocVien model = new HocVien();
                model.setMaNH(maNH);
                model.setDiem(-1);
                model.setMaKH(maKH);
                hocVienDAO.insert(model);
                sb.append("Thêm thành công học viên " + maNH + "\n");
            }
        }
        DialogHelper.alert(this, sb.toString());
        loadTable_hocVien();
    }

    @Override
    public void update() {
        double diem;
        for (int i = 0; i < lst_hocVien.size(); i++) {
            try {
                diem = Double.parseDouble(tbl_danhSachHocVien.getValueAt(i, 4).toString());
                if (lst_hocVien.get(i).getDiem() != diem) {
                    lst_hocVien.get(i).setDiem(diem);
                    hocVienDAO.update(lst_hocVien.get(i));
                }
            } catch (Exception e) {                
            }
        }
    }

    @Override
    public void delete() {
        int[] rowIndex_hocVien = tbl_danhSachHocVien.getSelectedRows();
        List<Integer> lst_maHV = new ArrayList<>();
        for (int i : rowIndex_hocVien) {
            lst_maHV.add(lst_hocVien.get(i).getMaHV());
        }
        for (Integer maHV : lst_maHV) {
            hocVienDAO.delete(maHV);
        }
        loadTable_hocVien();
    }

